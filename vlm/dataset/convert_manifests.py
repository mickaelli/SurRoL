#!/usr/bin/env python
"""SurRoL-VLA · Standalone Manifest to ShareGPT Converter

Use this script in your modern (PyTorch) environment to convert 
the raw `manifest.jsonl` files generated by SurRoL into the multimodal 
`training_data.json` format required by Qwen-VL.

Usage
-----
    python vlm/dataset/convert_manifests.py \
        --out vlm/dataset \
        --tasks StaticTrack NeedlePick
"""

import argparse
import json
import sys
from pathlib import Path

_PROJECT_ROOT = Path(__file__).resolve().parents[2]
if str(_PROJECT_ROOT) not in sys.path:
    sys.path.insert(0, str(_PROJECT_ROOT))

from vlm.dataset.data_processing import convert_manifest_to_sharegpt

try:
    from surrol.wrappers.surrol_sb3 import TASK_NAME_TO_ID  # type: ignore
    _ID_TO_TASK_KEY = {v: k for k, v in TASK_NAME_TO_ID.items()}
except Exception:
    _ID_TO_TASK_KEY = {}


def _resolve_manifest_path(base_dir: Path, task: str) -> tuple[str, Path] | None:
    """Resolve manifest.jsonl path for a task across naming styles.

    Supports:
      - snake_case dirs: expert_static_track (preferred)
      - legacy dirs:     expert_statictrack
    """
    task_id = task if task.endswith("-v0") else f"{task}-v0"

    candidates: list[str] = []
    task_key = _ID_TO_TASK_KEY.get(task_id)
    if task_key:
        candidates.append(task_key)
    candidates.append(task_id.replace("-v0", "").lower())

    for tag in dict.fromkeys(candidates):
        manifest_path = base_dir / f"expert_{tag}" / "manifest.jsonl"
        if manifest_path.exists():
            return task_id, manifest_path
    return None


def main():
    parser = argparse.ArgumentParser(description="Convert JSONL manifests to ShareGPT format")
    parser.add_argument("--out", type=Path, default=Path("vlm/dataset"),
                        help="Base directory where expert_task folders are located")
    parser.add_argument("--tasks", type=str, nargs="+", default=[],
                        help="List of task names to process (e.g., StaticTrack NeedlePick). If empty, auto-detects all expert_ folders.")
    args = parser.parse_args()

    # 1. Find manifest files
    manifests_to_process = []
    
    if args.tasks:
        for task in args.tasks:
            resolved = _resolve_manifest_path(args.out, task)
            if resolved is not None:
                task_id, manifest_path = resolved
                manifests_to_process.append((task_id, manifest_path))
            else:
                print(f"[Warning] Manifest not found for {task} under {args.out} (tried snake_case + legacy)")
    else:
        # Auto-detect
        for d in args.out.glob("expert_*"):
            if d.is_dir() and (d / "manifest.jsonl").exists():
                task_name = d.name.replace("expert_", "")
                manifests_to_process.append((task_name, d / "manifest.jsonl"))

    if not manifests_to_process:
        print("No manifest.jsonl files found! Please run export_expert_universal.py first.")
        return

    all_training_data_paths = []

    # 2. Process each manifest
    print(f"\n{'='*60}")
    print(f"  Converting Manifests to ShareGPT Format")
    print(f"{'='*60}")
    
    for task_name, manifest_path in manifests_to_process:
        task_tag = manifest_path.parent.name.replace("expert_", "")
        print(f"\nProcessing: {task_name}")
        
        # Read the first line to extract the instruction
        instruction = f"Complete the {task_name} task."
        with manifest_path.open("r", encoding="utf-8") as f:
            for line in f:
                if line.strip():
                    item = json.loads(line)
                    instruction = item.get("instruction", instruction)
                    break
        
        out_dir = manifest_path.parent
        training_data_path = out_dir / "training_data.json"
        
        # Use CONTINUOUS mode to prevent action scale mismatch during inference
        # (Generates raw float actions without normalizing)
        convert_manifest_to_sharegpt(
            manifest_path=manifest_path,
            output_path=training_data_path,
            user_prompt=instruction,
            mode="continuous",
            precision=3,
        )
        
        all_training_data_paths.append({
            "task": task_name,
            "task_tag": task_tag,
            "training_data": training_data_path
        })
        print(f"  → Saved to {training_data_path}")

    # 3. Merge training data for multi-task training
    if len(all_training_data_paths) > 1:
        print(f"\n{'='*60}")
        print("  Merging multi-task training data...")
        merged = []
        for r in all_training_data_paths:
            with open(r["training_data"], "r", encoding="utf-8") as f:
                data = json.load(f)
                
                # Prefix IDs with task name to avoid collisions
                task_tag = r["task_tag"]
                for item in data:
                    item["id"] = f"{task_tag}-{item['id']}"
                    item["task"] = r["task"]  # add task label
                    
                    # Fix image paths so they become relative to --out (vlm/dataset)
                    if "images" in item:
                        item["images"] = [f"expert_{task_tag}/{img}" for img in item["images"]]
                        
                merged.extend(data)

        merged_path = args.out / "training_data_multitask.json"
        with merged_path.open("w", encoding="utf-8") as f:
            json.dump(merged, f, ensure_ascii=False, indent=2)
        print(f"  Merged {len(merged)} samples → {merged_path}")
        print(f"{'='*60}")

    print("\nConversion Complete!")

if __name__ == "__main__":
    main()
