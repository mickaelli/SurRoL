#!/usr/bin/env python
"""SurRoL-VLA · Learning Curve Plotter for RLAIF Experiments.

Reads the `experiment_metrics.csv` generated by training runs
and plots moving averages of:
  1. True Environment Reward vs Timesteps
  2. Success Rate vs Timesteps
"""

import argparse
import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns
from pathlib import Path

def smooth_series(series, window, min_periods=1):
    """Compute centered rolling average."""
    return series.rolling(window=window, min_periods=min_periods, center=True).mean()

def main():
    parser = argparse.ArgumentParser(description="Plot RLAIF vs Baseline learning curves")
    parser.add_argument("--baseline-dir", type=str, required=True)
    parser.add_argument("--dense-human-dir", type=str, required=True)
    parser.add_argument("--rlaif-dir", type=str, required=True)
    parser.add_argument("--output", type=str, default="rlaif_ablation.png")
    parser.add_argument("--window", type=int, default=50, help="Moving average window size")
    args = parser.parse_args()

    sns.set_theme(style="darkgrid", context="talk")

    fig, axes = plt.subplots(1, 2, figsize=(16, 6))

    labels = ["Baseline (Sparse Reward)", "Engineered (Dense Distance)", "RLAIF (Dense VLM Score)"]
    dirs = [args.baseline_dir, args.dense_human_dir, args.rlaif_dir]
    colors = ["#e74c3c", "#f39c12", "#2ecc71"]  # Red, Orange, Green

    for label, d, color in zip(labels, dirs, colors):
        csv_path = Path(d) / "experiment_metrics.csv"
        if not csv_path.exists():
            print(f"Warning: {csv_path} not found. Skipping {label}.")
            continue

        df = pd.read_csv(csv_path)
        if len(df) == 0:
            print(f"Warning: {csv_path} is empty.")
            continue

        # Convert success to float 0.0 / 1.0 just in case
        df["success"] = df["success"].astype(float)

        # Smooth the data
        df["smoothed_reward"] = smooth_series(df["ep_env_reward"], args.window)
        df["smoothed_success"] = smooth_series(df["success"], args.window)

        # Plot Reward
        axes[0].plot(df["step"], df["smoothed_reward"], label=label, color=color, linewidth=2.5)
        axes[0].plot(df["step"], df["ep_env_reward"], color=color, alpha=0.15)

        # Plot Success Rate
        axes[1].plot(df["step"], df["smoothed_success"], label=label, color=color, linewidth=2.5)

    # Styling Axis 0: Reward
    axes[0].set_title("True Environment Reward vs. Timesteps", pad=15, fontweight="bold")
    axes[0].set_xlabel("Environment Steps")
    axes[0].set_ylabel("Episodic Reward")
    axes[0].legend(loc="lower right")

    # Styling Axis 1: Success Rate
    axes[1].set_title("Task Success Rate vs. Timesteps", pad=15, fontweight="bold")
    axes[1].set_xlabel("Environment Steps")
    axes[1].set_ylabel("Success Rate")
    axes[1].set_ylim([-0.05, 1.05])
    
    import matplotlib.ticker as mtick
    axes[1].yaxis.set_major_formatter(mtick.PercentFormatter(1.0))
    axes[1].legend(loc="lower right")

    plt.suptitle("Ablation Study: Sparse vs Dense Human vs Dense VLM Feedback", 
                 fontsize=20, fontweight="bold", y=1.05)
    
    plt.tight_layout()
    plt.savefig(args.output, dpi=300, bbox_inches="tight")
    print(f"Plot saved successfully to {args.output}")

if __name__ == "__main__":
    main()
